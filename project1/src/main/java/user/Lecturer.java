package user;

public class Lecturer {
    private int lecturer_id;
    public int getLecturer_id() {return lecturer_id;}
    public void setLecturer_id(int lecturer_id) {this.lecturer_id = lecturer_id;}

    private String name;
    public String getName() {return name;}
    public void setName(String name) {this.name = name;}

    private int major_id;
    public int getMajor_id() {return major_id;}
    public void setMajor_id(int major_id) {this.major_id = major_id;}

    private String dept_name;
    public String getDept_name() {return dept_name;}
    public void setDept_name(String dept_name) {this.dept_name = dept_name;}
}
